package com.suttori.entity.MangaDesu;

public class MangaImage {

    private String original;
    private String preview;
    private String x120;
    private String x225;
    private String x48;
    private String x32;

    public String getX32() {
        return x32;
    }

    public void setX32(String x32) {
        this.x32 = x32;
    }

    public String getX48() {
        return x48;
    }

    public void setX48(String x48) {
        this.x48 = x48;
    }

    public String getX225() {
        return x225;
    }

    public void setX225(String x225) {
        this.x225 = x225;
    }

    public String getOriginal() {
        return original;
    }

    public void setOriginal(String original) {
        this.original = original;
    }

    public String getPreview() {
        return preview;
    }

    public void setPreview(String preview) {
        this.preview = preview;
    }

    public String getX120() {
        return x120;
    }

    public void setX120(String x120) {
        this.x120 = x120;
    }
}
